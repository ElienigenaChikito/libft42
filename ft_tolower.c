#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	return (c);
}
// Funcion simple, suma 32 bits al caracter proporcionado,
// para que cuadre con su posicion en "Minuscula" de la tabla ASCII.