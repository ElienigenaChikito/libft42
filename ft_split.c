#include "libft.h"

char    **ft_split(const char *s, char c)
{
    int i;

    if (s == NULL)
        return (NULL);
    i = 0;
    while (s[i] != '\0')
    {
        while (s[i] != c)
        {
            
            i++;
        }
    }
}